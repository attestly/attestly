  let currentRoot = null;
  function mount(funderId) {
    const funder = FUNDERS.find((f) => f.id === funderId) || FUNDERS[0];
    if (!funder) {
      console.warn('[grant-form-helper] no funders embedded');
      return;
    }
    let md;
    try {
      md = b64ToUtf8(funder.b64);
    } catch (err) {
      console.error('[grant-form-helper] base64 decode failed', err);
      return;
    }
    const sections = parseSections(md);
    if (currentRoot) {
      if (typeof currentRoot._gfhTeardown === 'function') currentRoot._gfhTeardown();
      if (currentRoot.parentNode) currentRoot.parentNode.removeChild(currentRoot);
    }
    currentRoot = buildSidebar(sections, FUNDERS, funder.id, (newId) => mount(newId));
    document.body.appendChild(currentRoot);
  }

  function init() {
    injectStyleOnce();
    mount(pickFunderForLocation());
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init, { once: true });
  } else {
    init();
  }
