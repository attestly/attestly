  let toastTimer = null;
  function showToast(msg) {
    let toast = document.querySelector('.gfh-toast');
    if (!toast) {
      toast = document.createElement('div');
      toast.className = 'gfh-toast';
      document.body.appendChild(toast);
    }
    toast.textContent = msg;
    toast.classList.add('gfh-show');
    if (toastTimer) clearTimeout(toastTimer);
    toastTimer = setTimeout(() => toast.classList.remove('gfh-show'), 1500);
  }

  // Best-effort label extraction for a focused form field — used by the
  // stretch fuzzy-match feature. Read-only; never mutates the page.
  function describeField(el) {
    if (!el) return '';
    const parts = [];
    if (el.id) {
      const lbl = document.querySelector(`label[for="${CSS.escape(el.id)}"]`);
      if (lbl && lbl.textContent) parts.push(lbl.textContent);
    }
    if (el.getAttribute) {
      for (const a of ['aria-label', 'placeholder', 'name', 'data-field', 'data-name']) {
        const v = el.getAttribute(a);
        if (v) parts.push(v);
      }
    }
    // Walk up a few ancestors looking for a wrapping label.
    let node = el.parentElement;
    let hops = 0;
    while (node && hops < 4) {
      if (node.tagName === 'LABEL') {
        if (node.textContent) parts.push(node.textContent);
        break;
      }
      node = node.parentElement;
      hops++;
    }
    return parts.join(' ').replace(/\s+/g, ' ').trim();
  }

