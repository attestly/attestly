  function pickFunderForLocation() {
    const href = location.href;
    for (const f of FUNDERS) {
      for (const pat of f.matches) {
        // Convert a Tampermonkey @match-style pattern (with one '*' for path)
        // into a simple prefix check. Good enough for our portal whitelist.
        const prefix = pat.replace(/\*+$/, '');
        if (href.startsWith(prefix)) return f.id;
      }
    }
    return FUNDERS[0] && FUNDERS[0].id;
  }

