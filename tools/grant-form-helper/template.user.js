  /**
   * Parse a markdown blob into [{ heading, body, wordCount }, ...]:
   *   - Each H2 ("## ...") opens a new section.
   *   - H1 ("# ...") lines are treated as scope markers (e.g. "TAB 1 — ...")
   *     and are NOT separate fields. The most recent H1 is attached to each
   *     following section as `.scope` so the sidebar can show "TAB 1 / Field".
   *   - H3+ lines stay inside their parent H2 body.
   *   - Leading/trailing whitespace in each body is trimmed.
   */
  function parseSections(md) {
    const lines = md.split(/\r?\n/);
    const sections = [];
    let scope = null;
    let current = null;

    for (const line of lines) {
      const h1 = /^#\s+(.+?)\s*$/.exec(line);
      const h2 = /^##\s+(.+?)\s*$/.exec(line);
      if (h1 && !h2) {
        scope = h1[1].trim();
        continue;
      }
      if (h2) {
        if (current) sections.push(current);
        current = {
          heading: h2[1].trim(),
          scope,
          bodyLines: [],
        };
        continue;
      }
      if (current) {
        current.bodyLines.push(line);
      }
    }
    if (current) sections.push(current);

    return sections.map((s, idx) => {
      const body = s.bodyLines.join('\n').replace(/^\s+|\s+$/g, '');
      return {
        index: idx,
        heading: s.heading,
        scope: s.scope,
        body,
        wordCount: countWords(body),
      };
    });
  }

  /** Strip markdown wrappers so the clipboard payload is closer to "what you
   *  want to paste into the textarea". Specifically:
   *    - Single fenced code blocks ("```...```") that fill the body become
   *      their inner content (handles the URL/title-style fields).
   *    - Leading/trailing whitespace is trimmed.
   *    - Word-count footer lines ("**~165 words.**") are stripped — they're
   *      meta, not field content.
   */
  function clipboardPayload(body) {
    if (!body) return '';
    let out = body;
    // Strip trailing word-count notes like "**~175 words.**" or "**~95 words.**"
    out = out.replace(/\n+\s*\*\*~\d+\s+words?\.?\*\*\s*$/i, '');
    // If the whole body is a single fenced code block, unwrap it.
    const wholeFence = /^\s*```[^\n]*\n([\s\S]*?)\n?```\s*$/m.exec(out.trim());
    if (wholeFence && wholeFence[0].trim() === out.trim()) {
      out = wholeFence[1];
    }
    return out.replace(/^\s+|\s+$/g, '');
  }

