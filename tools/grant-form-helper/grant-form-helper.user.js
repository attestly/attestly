  /** Decode base64 to a UTF-8 string. atob returns latin1; we go via Uint8Array
   *  so multi-byte characters in the markdown (e.g. em-dashes, accents) survive. */
  function b64ToUtf8(b64) {
    const bin = atob(b64);
    const bytes = new Uint8Array(bin.length);
    for (let i = 0; i < bin.length; i++) bytes[i] = bin.charCodeAt(i);
    return new TextDecoder('utf-8').decode(bytes);
  }

  /** Count words in a string, ignoring markdown fences and link-only lines. */
  function countWords(s) {
    if (!s) return 0;
    // Strip fenced code blocks for the count — they're usually short values
    // (URLs, titles) and we still want to count them, so include them.
    // Strip markdown emphasis markers and headings.
    const cleaned = s
      .replace(/```[\s\S]*?```/g, (block) => block.replace(/```/g, ''))
      .replace(/[*_#>`]/g, ' ');
    const m = cleaned.match(/\S+/g);
    return m ? m.length : 0;
  }

  /** Very small Levenshtein implementation — used only for short field names
   *  (≤ ~80 chars) so the O(n*m) table is fine. */
  function levenshtein(a, b) {
    a = a.toLowerCase();
    b = b.toLowerCase();
    if (a === b) return 0;
    if (!a.length) return b.length;
    if (!b.length) return a.length;
    const prev = new Array(b.length + 1);
    const curr = new Array(b.length + 1);
    for (let j = 0; j <= b.length; j++) prev[j] = j;
    for (let i = 1; i <= a.length; i++) {
      curr[0] = i;
      for (let j = 1; j <= b.length; j++) {
        const cost = a.charCodeAt(i - 1) === b.charCodeAt(j - 1) ? 0 : 1;
        curr[j] = Math.min(curr[j - 1] + 1, prev[j] + 1, prev[j - 1] + cost);
      }
      for (let j = 0; j <= b.length; j++) prev[j] = curr[j];
    }
    return prev[b.length];
  }

  /** Similarity in [0,1]; 1 means identical. Combines substring containment
   *  (weighted heavily) with normalised Levenshtein. */
  function similarity(needle, hay) {
    if (!needle || !hay) return 0;
    const a = needle.toLowerCase().trim();
    const b = hay.toLowerCase().trim();
    if (!a || !b) return 0;
    if (a === b) return 1;
    if (b.includes(a) || a.includes(b)) {
      return 0.85 + 0.15 * (Math.min(a.length, b.length) / Math.max(a.length, b.length));
    }
    const dist = levenshtein(a, b);
    const maxLen = Math.max(a.length, b.length);
    return 1 - dist / maxLen;
  }

